n = int(input())

nn = n

k = 1
while nn//10:
    k += 1
    nn = nn//10

ans = 0

if k == 1:
    ans = n
elif k == 2:
    ans = 9
    n10 = n//10
    n1 = n-n10
    if n10 <= n1:
        ans += n10*n10
        ans += n10*2
    else:
        ans += n10*(n10-1)+n1+(n10-1)*2
elif k == 3:
    ans = 99+9
    n100 = n//100
    n10 = (n-n100)//10
    n1 = n-n100-n10
    if n100 <= n1:
        ans += n100*n100*110*2+10*n100
    else:
        ans += n100*n1*110*2+10*n1-(100-10*(n10+1))*n1
elif k == 4:
    ans = 999+99+9
    n1000 = n//1000
    n100 = (n-n1000)//100
    n10 = (n-n1000-n100)//10
    n1 = n-n1000-n100-n10
    if n1000 <= n1:
        ans += n1000*(n1000-1)*100
        ans += n1000*n1000*(10*n100+n10+1)
    elif n1 <= n1000 and n1 != 0:
        ans += (n1-1)*(n1-1)*100
        ans += n1*n1*(10*n100+n10+1)
elif k == 5:
    ans = 9999+999+99+9
    n5 = n//10000
    n4 = (n-n5)//1000
    n3 = (n-n5-n4)//100
    n2 = (n-n5-n4-n3)//10
    n1 = n-n5-n4-n3-n2
    if n5 < n1:
        ans += (n5-1)*(n5-1)*1000
        ans += n5*n5*(100*n4+10*n3+n2+1)
    elif n1 <= n5 and n1 != 0:
        ans += (n1-1)*(n1-1)*100
        ans += n1*n1*(100*n4+10*n3+n2+1)
else:
    ans = 99999+9999+999+99+9
    n6 = n//100000
    n5 = (n-n6)//10000
    n4 = (n-n6-n5)//1000
    n3 = (n-n6-n5-n4)//100
    n2 = (n-n6-n5-n4-n3)//10
    n1 = n-n6-n5-n4-n3-n2
    if n6 < n1:
        ans += (n6-1)*(n6-1)*10000
        ans += n6*n6*(1000*n5+100*n4+10*n3+n2+1)
    elif n1 <= n6 and n1 !=0:
        ans += (n1-1)*(n1-1)*10000
        ans += n1*n1*(1000*n5+100*n4+10*n3+n2+1)

